/*

statements vs expressions-> c# differentiates between the two.

a statement is any action that the program takes. things such as variable
assignment, calling methods, if/else/switch/case and loops/iterations/jumps/exceptions
 are all statements.

an expression is something that can be evaluated into a single value. This includes
something like x > 5 because this would evaluate to either true or false.
There are no side effects with expressions.

source(statement): https://msdn.microsoft.com/en-us/library/ms173143.aspx
source(expression): https://msdn.microsoft.com/en-us/library/ms173144.aspx

*/
